command in powershell is called commandlet or cmdlet. (pronunced as Command-Let)
commandlet is lightwet powershell script that performs a single task
commandlet in powershell is always in "Verb-Noun" form. (Verb and Noun separated by dash)

ex:
Get-Service

the above commandlet on execution gives the list of all services in your system
Get is Verb
Service is Noun

