<#

"Get-Help" is built in rich and user friendly commandlet / feature of powershell
learning to use Get-Help commandlet can help you to learn powershell

#>

# examples:

# to download and install "Help" files for the module that includes this cmdlet, execute below:

Update-Help

# to see commandlets related to service, execute below:

Get-Help *Service*

# to see how you can use "Get-Service" commandlet and to see its sysntax, execute below:

Get-Help Get-Service